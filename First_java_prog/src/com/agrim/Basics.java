package com.agrim;

public class Basics {
    public static void main(String[] args) {

        int a = 10;
        if (a == 10) {
            System.out.println("hello with basic if");
        }

        int v = 0;
        while (v != 5) {
            System.out.println("v with basic while");
            v++;
        }
        for (int count = 0; count != 5; count++) {
            System.out.println("Hello there number " + count + " with basic for");
        }


    }
}


